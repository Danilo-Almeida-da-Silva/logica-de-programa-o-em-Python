#DICIONARIO

## Os dicionarios representam coleções de dados que contém na sua estrutura.

pessoas = ['danilo', 'ana', 'joao'] #Listas


pessoas = {'pedro': 18, 'luana' : 20} #Dicionarios


print(pessoas['pedro'])

print(pessoas['luana'])


pessoas.keys() #mostra as chaves

pessoas.values() #mostra valores


pessoas.items() # mostra todo dicionário

pessoas.pop #revome e mostra o ultimo item removido

