#Escreva um algoritmo que lê dois valores booleanos (lógicos) e então determina se ambos são VERDADEIROS ou FALSOS.

valor1 = bool(input('digite qualquer valor booleanos : ' ))
valor2 = bool(input('digite qualquer valor booleanos :'))

if valor1 != valor2:
  print('os dois valores são verdadeiros')
else:
  valor1 == valor2
  print('os dois valores são falsos')